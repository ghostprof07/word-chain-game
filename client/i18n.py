_aktif = 'en'


def dil_ayarla(kod):
    global _aktif
    if kod in CEVIRILER:
        _aktif = kod


def aktif_dil():
    return _aktif


def t(anahtar, **kw):
    """Anahtarın aktif dildeki karşılığını döndürür; {param} alanlarını doldurur."""
    metin = CEVIRILER.get(_aktif, CEVIRILER['en']).get(anahtar)
    if metin is None:
        metin = CEVIRILER['en'].get(anahtar, anahtar)
    if kw:
        try:
            metin = metin.format(**kw)
        except Exception:
            pass
    return metin
